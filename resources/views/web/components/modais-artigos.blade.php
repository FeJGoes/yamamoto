{{-- APRENDENDO COM 3º SETOR  --}}
<div id="modal-article-terceiro-setor" uk-modal>
  <div class="uk-modal-dialog uk-modal-body ty-modal-body-article">
    <button class="uk-modal-close-default" type="button" uk-close></button>
    <h2 class="uk-modal-title">Aprendendo com o 3º Setor</h2>

    <p>Antes de mais de nada é preciso primeiramente explicar o que é o 1º, 2° e 3° setor. O primeiro setor é comumente
      conhecido como setor público, segundo setor é o setor privado.</p>

    <p>Já o terceiro setor é conhecido por ONG’s ou Associações em geral. O terceiro setor surge da necessidade local e
      da incapacidade do poder público de atender por completo todas as demandas da sociedade.</p>

    <p>Em especial o 3º setor ele pode dar mais liberdade aos indivíduos para atuar de maneira mais incisiva e
      contundente na sociedade. Um exemplo muito claro deste tipo de organização são os lares para idosos como o Asilo
      São Vicente e o Lar Bethel aqui de Rio Claro.</p>

    <p>Mas por qual motivo eles são organizações de 3º setor? Pelo simples fato de não serem totalmente dependentes do
      poder público tendo assim uma maior automia.</p>

    <p>Agora outra pergunta que pode surgir é como estas organizações são mantidas ja que não recebem verba
      governamental? Ai é que está a grande sacada, elas se mantém a partir da solidariedade e preocupação da sociedade
      local em resolver seus problemas locais de maneira eficiente.</p>
  </div>
