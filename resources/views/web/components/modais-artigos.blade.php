{{-- O QUE É UMA CPI --}}
<div id="modal-article-cpi" uk-modal>
  <div class="uk-modal-dialog uk-modal-body ty-modal-body-article">
    <button class="uk-modal-close-default" type="button" uk-close></button>
    <h2 class="uk-modal-title">O Que é Uma CPI?</h2>

    <p>A Comissão Parlamentar de Inquérito é um organismo que tem como finalidade proteger os interesses da população.
      Essa proteção ocorre na forma de uma investigação sobre fatos ocorridos na administração pública.</p>

    <p>A CPI é uma investigação que é feita pelo poder legislativo municipal estadual ou federal e possui acesso ao
      funcionamento da máquina pública, pode convocar pessoas para depor requisitar informações sigilosas e tomar
      providências caso seja necessário.</p>

    <p>Ao término da CPI caso realmente existam irregularidades o inquérito é então encaminhado ao ministério público e
      desta maneira a investigação tem continuidade até o seu desfecho e a punição dos culpados.</p>

    <p>A CPI esta descrita no Artigo 58 parágrafo 3º constiutição federal de 1988 e na seção III Artigo 36 do regimento
      interno da Camara Municipal de rio claro.</p>
  </div>
</div>

