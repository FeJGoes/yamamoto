      <dd>
        <p>O Judô é uma arte marcial e esporte que foi fundada por Jigoro Kano em 1882. Os seus principais objetivos são
          fortalecer o físico, a mente e o espírito de forma integrada, além de desenvolver técnicas de defesa pessoal.
        </p>

        <p>Judô significa ao pé da letra Caminho Suave e seu faz jus ao estilo de combate que ele proporciona, onde seus
          praticantes aprendem antes de tudo a cair e levantar. Facilitando assim no momento em que recebem os golpes a
          não se machucar.</p>
      </dd>
      <dt>Benefícios</dt>
      <dd>
        <p>A prática do judo trás consigo vários beneficios para o praticante desta arte. Dentre os principais estão à
          melhora na saúde e a prática das virtudes samurais.</p>

        <p>No ambito da saúde inevitavelmente ajuda o praticante a ter um melhor condicionamento físico, melhora dos
          reflexos, e principalmente o fato de se aprender a cair sem machucar torna a prática do judo essencial para
          uma vida melhor.</p>

        <p>Já no ambito social o judô e outras artes marcias japonesas trazem consigo toda filosofia do BUSHIDO, que é o
          caminho samurai. Uma filosofia de vida permeada de honra e verdade, que são valores imprecindivéis para a
          prática da cidadania.</p>
