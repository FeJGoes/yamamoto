    <h2 class="uk-modal-title">O Problema do Excesso de Leis</h2>

    <p>Relativo à criação de leis,o Brasil como um todo se tem a deia errônea de que o bom arlamentar é aquele que cria
      uitas leis, no entanto esse ensamento já foi superado na aioria dos países desenvolvidos. que acontece é que ao se
      criar uitas leis e muitas delas sem a evida análise acaba-se criando m complexo sistema jurídico com uitas regras,
      que por fim deixa a ociedade perdida sem saber o ue seguir ao certo.</p>

    <p>Esta complexidade atrapalha o empreendedor que acaba por gastar muito em contabilidade sobrando menos tempo e
      recurso para o desenvolvimento de seu negócio e consequente desenvolvimento do país, através da geração de emprego
      e renda.</p>

    <p>Para exemplificar essa situação como noticiado pelo G1, o advogado Vinícios Leôncio publicou um livro de 7,5
      toneladas contendo apenas a legislação tributária brasileira, se formos somar o restante das leis brasileiras o
      livro seria muito maior.</p>
