    <h2 class="uk-modal-title">Os três poderes</h2>

    <p>A separação dos três poderes é uma teoria desenvolvida por Charles de Montesquieu, na obra o "O Espírito das
      Leis" (1748), foi criada a partir da influência de outros pensadores anteriores a ele, como Aristóteles na obra
      intitulada "Política" e posteriormente por John Locke. Porém, nenhuma obra anterior explica de forma mais coerente
      e detalhada a estruturação do estado regido sob os três poderes (Executivo, Legislativo e Judiciário) como a de
      Charles-Louis de Secondat (ou Montesquieu).</p>
